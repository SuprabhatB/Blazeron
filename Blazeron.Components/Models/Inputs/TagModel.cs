namespace Blazeron.Components.Models.Inputs
{
    /// <summary>
    /// Defines the <see cref="TagModel" />.
    /// </summary>
    public class TagModel
    {
        /// <summary>
        /// Gets or sets the name.
        /// </summary>
        /// <value>
        /// The name.
        /// </value>
        public string Name { get; set; }

        /// <summary>
        /// Gets or sets a value indicating whether this instance is show validation.
        /// </summary>
        /// <value>
        ///   <c>true</c> if this instance is show validation; otherwise, <c>false</c>.
        /// </value>
        public bool IsShowValidation { get; set; }
    }
}
